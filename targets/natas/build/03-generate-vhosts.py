#!/usr/bin/env python3
"""Generates one Apache <VirtualHost> per Natas level (port 8000+N,
matching a single internal hostname rather than OTW's real subdomain
scheme -- this box has no externally resolvable name of its own, it's
reached only via the range's internal hostname, so port-per-level is the
practical adaptation; see the blueprint's Phase 4 notes), each with its
own AssignUserID (MPM-ITK) and HTTP Basic Auth htpasswd file.

Security: levels 1-14's htpasswd files are placeholder/invalid at build
time -- entrypoint.sh regenerates them with real per-team secrets at
container START (see docs/security-audit-status.md). Only level 0's
password is real here: it's OverTheWire's public well-known bootstrap
credential, not a secret, so there's nothing to rotate per team."""
import os
import subprocess

WEBPASS = {0: "natas0"}
# levels 1-14 get an invalid, unguessable build-time placeholder --
# entrypoint.sh overwrites this with a real per-team secret. Never a
# valid credential on its own (deliberately not a real password shape).
for _n in range(1, 15):
    WEBPASS[_n] = "BUILD_TIME_PLACEHOLDER_NOT_A_REAL_PASSWORD"

os.makedirs("/etc/apache2/natas-htpasswd", exist_ok=True)
os.makedirs("/etc/apache2/sites-available", exist_ok=True)

ports_lines = ["# Generated by 03-generate-vhosts.py -- one Listen per Natas level.\n"]
vhost_enable_cmds = []

for n in range(15):
    port = 8000 + n
    user = f"natas{n}"
    docroot = f"/var/www/natas/natas{n}"
    htpasswd_path = f"/etc/apache2/natas-htpasswd/natas{n}"

    subprocess.run(
        ["htpasswd", "-bBc", htpasswd_path, user, WEBPASS[n]],
        check=True,
        stdout=subprocess.DEVNULL,
    )
    # Security: htpasswd's own default mode (644, root-owned) left every
    # level's bcrypt hash file world-readable to any other level's MPM-ITK
    # worker (each vhost's AssignUserID runs as a DIFFERENT natasN user,
    # but they share the filesystem) -- a player with code execution at
    # any one level (e.g. the intended natas12/13 upload RCE) could read
    # every OTHER level's htpasswd file directly, not just the next one
    # via the intended /etc/natas_webpass leak chain (which is correctly
    # scoped 640, ownerN:group(N-1) -- see 02-set-webpasswords.sh). Only
    # this level's own Apache worker (AssignUserID {user} {user}) ever
    # legitimately needs to read this file, so lock it down to owner-only.
    subprocess.run(["chown", f"{user}:{user}", htpasswd_path], check=True)
    os.chmod(htpasswd_path, 0o600)

    ports_lines.append(f"Listen {port}\n")

    vhost_conf = f"""<VirtualHost *:{port}>
    ServerName natas{n}.local
    DocumentRoot {docroot}
    AssignUserID {user} {user}
    php_admin_value auto_prepend_file /opt/cei-natas-banner.php

    <Directory {docroot}>
        Options FollowSymLinks
        AllowOverride All

        AuthType Basic
        AuthName "Natas{n}"
        AuthUserFile {htpasswd_path}
        Require user {user}
    </Directory>

    ErrorLog ${{APACHE_LOG_DIR}}/natas{n}-error.log
    CustomLog ${{APACHE_LOG_DIR}}/natas{n}-access.log combined
</VirtualHost>
"""
    conf_path = f"/etc/apache2/sites-available/natas{n}.conf"
    with open(conf_path, "w", newline="\n") as f:
        f.write(vhost_conf)
    vhost_enable_cmds.append(f"natas{n}")

# Disable Apache's own default site/port entirely -- only our explicit
# per-level ports should ever answer.
subprocess.run(["a2dissite", "000-default"], check=False)
with open("/etc/apache2/ports.conf", "w", newline="\n") as f:
    f.writelines(ports_lines)

for name in vhost_enable_cmds:
    subprocess.run(["a2ensite", name], check=True, stdout=subprocess.DEVNULL)

subprocess.run(["a2enmod", "rewrite"], check=True, stdout=subprocess.DEVNULL)

print("Generated and enabled 15 Natas vhosts (ports 8000-8014).")
